package pacote.metodos;

public class Relatorio {

	private String nomeRelatorio;
	private String descricaoRelatorio;
	private String dataInicioRelatorio;
	private String dataFinalRelatorio;
	private int idRelatorio;

	public String getNomeRelatorio() {
		return nomeRelatorio;
	}

	public void setNomeRelatorio(String nomeRelatorio) {
		this.nomeRelatorio = nomeRelatorio;
	}

	public String getDescricaoRelatorio() {
		return descricaoRelatorio;
	}

	public void setDescricaoRelatorio(String descricaoRelatorio) {
		this.descricaoRelatorio = descricaoRelatorio;
	}

	public String getDataInicioRelatorio() {
		return dataInicioRelatorio;
	}

	public void setDataInicioRelatorio(String dataInicioRelatorio) {
		this.dataInicioRelatorio = dataInicioRelatorio;
	}

	public String getDataFinalRelatorio() {
		return dataFinalRelatorio;
	}

	public void setDataFinalRelatorio(String dataFinalRelatorio) {
		this.dataFinalRelatorio = dataFinalRelatorio;
	}

	public int getIdRelatorio() {
		return idRelatorio;
	}

	public void setIdRelatorio(int idRelatorio) {
		this.idRelatorio = idRelatorio;
	}

}
