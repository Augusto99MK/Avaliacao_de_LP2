package pacote.metodos;

public class Deploy {

	private String nomeDeploy;
	private String descricaoDeploy;
	private String dataInicio;
	private String dataFinal;
	private int idDeploy;

	public void setNomeDeploy(String nomeDeploy) {
		this.nomeDeploy = nomeDeploy;

	}

	public void setDescricaoDeploy(String descricaoDeploy) {
		this.descricaoDeploy = descricaoDeploy;

	}

	public void setDataInicio(String dataInicio) {
		this.dataInicio = dataInicio;

	}

	public void setDataFinal(String dataFinal) {
		this.dataFinal = dataFinal;

	}

	public void setIdDeploy(int idDeploy) {
		this.idDeploy = idDeploy;
	}

	public String getNomeDeploy() {
		return nomeDeploy;
	}

	public String getDescricaoDeploy() {
		return descricaoDeploy;
	}

	public String getDataInicio() {
		return dataInicio;
	}

	public String getDataFinal() {
		return dataFinal;
	}

	public int getIdDeploy() {
		return idDeploy;
	}

}
